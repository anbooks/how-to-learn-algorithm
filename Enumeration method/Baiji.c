/*
公鸡每只5元、母鸡每只3元，小鸡3只1元。用100元钱买100只鸡，问公鸡、母鸡、小鸡各多少？
*/
#include <stdio.h>
int main()
{
    int x,y,z;  //定义变量，公鸡、母鸡、小鸡
    for(x=0; x<=20; x++)  //公鸡最多100/5=20只
    {
        for(y=0; y<=33; y++)  //母鸡最多33只
        {
            z=100-x-y;   //小鸡的数量受限于公鸡+母鸡+小鸡=100
            if(z%3==0&&x*5+y*3+z/3==100) //   价钱还需要符合规则
                printf("公鸡：%d,母鸡：%d,小鸡:%d\n",x,y,z);
        }
    }
    getch();   //程序执行完，不是直接结束，可以等用户再随便按键
    return 0;
}
